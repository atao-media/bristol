{
	int i, j;

	for (i = 0; i < 128; i++)
		font1[i] = &box;

	font1['A'] = &font1A;
	font1['a'] = &font1A;

	font1['B'] = &font1B;
	font1['b'] = &font1B;

	font1['C'] = &font1C;
	font1['c'] = &font1C;

	font1['D'] = &font1D;
	font1['d'] = &font1D;

	font1['E'] = &font1E;
	font1['e'] = &font1E;

	font1['F'] = &font1F;
	font1['f'] = &font1F;

	font1['G'] = &font1G;
	font1['g'] = &font1G;

	font1['H'] = &font1H;
	font1['h'] = &font1H;

	font1['I'] = &font1I;
	font1['i'] = &font1I;

	font1['J'] = &font1J;
	font1['j'] = &font1J;

	font1['K'] = &font1K;
	font1['k'] = &font1K;

	font1['L'] = &font1L;
	font1['l'] = &font1L;

	font1['M'] = &font1M;
	font1['m'] = &font1M;

	font1['N'] = &font1N;
	font1['n'] = &font1N;

	font1['O'] = &font1O;
	font1['o'] = &font1O;

	font1['P'] = &font1P;
	font1['p'] = &font1P;

	font1['Q'] = &font1Q;
	font1['q'] = &font1Q;

	font1['R'] = &font1R;
	font1['r'] = &font1R;

	font1['S'] = &font1S;
	font1['s'] = &font1S;

	font1['T'] = &font1T;
	font1['t'] = &font1T;

	font1['U'] = &font1U;
	font1['u'] = &font1U;

	font1['V'] = &font1V;
	font1['v'] = &font1V;

	font1['W'] = &font1W;
	font1['w'] = &font1W;

	font1['X'] = &font1X;
	font1['x'] = &font1X;

	font1['Y'] = &font1Y;
	font1['y'] = &font1Y;

	font1['Z'] = &font1Z;
	font1['z'] = &font1Z;

	font1['0'] = &font10;
	font1['1'] = &font11;
	font1['2'] = &font12;
	font1['3'] = &font13;
	font1['4'] = &font14;
	font1['5'] = &font15;
	font1['6'] = &font16;
	font1['7'] = &font17;
	font1['8'] = &font18;
	font1['9'] = &font19;

	font1['\''] = &quote;
	font1['%'] = &percent;
	font1['.'] = &dot;
	font1['-'] = &hiphen;
	font1['+'] = &plus;
	font1['/'] = &slash;
	font1['<'] = &left;
	font1['>'] = &right;

	/*
	 * Fix some of the widths, this was after the fact and I am not going to 
	 * redefine the whole font.
	 */
	for (i = 0; i < 128; i++)
	{
		for (j = 0; j < font1[i]->count; j++)
			if (font1[i]->coords[j].x >= 0)
				font1[i]->coords[j].x = font1[i]->coords[j].x * 17 / 20;
	}
}

